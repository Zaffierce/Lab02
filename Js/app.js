// Identifying global variables 

var numberOfCorrect = 0;
var numberOfQuestion = 1;

// creating a loop

function loop(guessAttemptsCounter, guessNumber, coreysQuestion, coreysAnswer, questionNumber) {
  var endLoop = false; 
  
  while (guessAttemptsCounter < guessNumber && endLoop === false) {
    var userAnswer = prompt(`Let\'s see how well you can do on a quiz all about me!\n\n Question ${numberOfQuestion}: ${coreysQuestion}`);
    for (var i = 0; i < coreysAnswer.length; i++) {
      if (userAnswer.toLowerCase() === coreysAnswer[i]) {
        alert('You are Right! Congratulations, keep going.');
        numberOfCorrect++;
        coreysQuestion;
        questionNumber;
        numberOfQuestion++;
        console.log(`Question loop : ${numberOfQuestion}`);
        endLoop = true;
        break;
      }
      guessAttemptsCounter++;
    }
  }
  if (guessAttemptsCounter > guessNumber) {
    endLoop = true;
    numberOfQuestion++;
    alert ('You are wrong! Keep going!');
  }
}


// Function of my favorite dog type 
function coreysFavoriteDog() {
  var questionNumber = 'First';
  var coreysQuestion = 'What is Corey\'s favorite type of dog?';
  var coreysAnswer = ['golden retriever'];
  var guessNumber = 3;
  var guessAttemptsCounter = 0;
  loop(guessAttemptsCounter, guessNumber, coreysQuestion, coreysAnswer, questionNumber);
}
